       public void GetBookNumber(){
              
              BalanceLabel.setEnabled(true);
              BalanceBox.setEnabled(true);
              
              BalanceBox.setText(StockBox.getText());
              
              try{
                            
                            Class.forName("com.mysql.jdbc.Driver");
                            
                            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");
                            
                            Statement stmt = con.createStatement();
                            String query = "select COUNT(*) from books;";
                            
                            ResultSet rs = stmt.executeQuery(query);
                            
                            while(rs.next()){
                                   
                                   BookNumberLabel.setEnabled(true);
                                   BookNumberBox.setEnabled(true);
                                   
                                   int bookNo = rs.getInt(1);
                                   
                                   BookNumberBox.setText(Integer.toString(bookNo + 1));
                            }
                            
                            rs.close();
                            stmt.close();
                            con.close();
                            
              }
              catch(Exception e){
                            
                            JOptionPane.showMessageDialog(null, "A database connection error occured.");              
              }
       }
       
