    private void AddBookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBookButtonActionPerformed
       if (!BookNameBox.getText().equals("") && !AuthorBox.getText().equals("") && !PublisherBox.getText().equals("") && !PageBox.getText().equals("") && !StockBox.getText().equals("")){
              
              GetBookNumber();
              
              try{
                     
                     Class.forName("com.mysql.jdbc.Driver");
                     Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");
                     
                     if (JOptionPane.showConfirmDialog(null, "Are you sure?") == JOptionPane.YES_OPTION){
                            
                            String query = "insert into books (bookNo, bookName, author, publisher, pages, stock, balance)" + " values (?, ?, ?, ?, ?, ?, ?);";
                            
                            PreparedStatement stmt = con.prepareStatement(query);
                            stmt.setString(1, BookNumberBox.getText());
                            stmt.setString(2, BookNameBox.getText());
                            stmt.setString(3, AuthorBox.getText());
                            stmt.setString(4, PublisherBox.getText());
                            stmt.setString(5, PageBox.getText());
                            stmt.setString(6, StockBox.getText());
                            stmt.setString(7, BalanceBox.getText());
                            stmt.execute();
                            
                            JOptionPane.showMessageDialog(null, "Book added successfully!");
                            
                            stmt.close();
                     }
                     
                     Clear();
                     
                     con.close();
              }
              catch(Exception e){
                     JOptionPane.showMessageDialog(null, "A database connection error occured.");     
              }
       }
       else{
              JOptionPane.showMessageDialog(null, "One or more than one fields are empty.");
       }
    }//GEN-LAST:event_AddBookButtonActionPerformed

