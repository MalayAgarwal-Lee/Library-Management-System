       public void SearchBook() {
              int c = 0;
              if (!"".equals(BookNameBox.getText())) {
                     try {
                            Class.forName("com.mysql.jdbc.Driver");
                            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");

                            Statement stmt = con.createStatement();

                            String query = "select bookNo, stock, balance from books where bookName = '" + BookNameBox.getText() + "';";

                            ResultSet rs = stmt.executeQuery(query);

                            while (rs.next()) {
                                   c = 1;
                                   if (rs.getInt(3) > 0 && rs.getInt(3) <= rs.getInt(2)) {
                                          
                                          AvailableBalanceBox.setEnabled(true);
                                          AvailableBalanceLabel.setEnabled(true);
                                          
                                          AvailableBalanceBox.setText(Integer.toString(rs.getInt(3)));
                                          
                                          BookNumberLabel.setEnabled(true);
                                          BookNumberBox.setEnabled(true);
                                          BookNumberBox.setText(Integer.toString(rs.getInt(1)));
                                          
                                          MemberNameBox.requestFocusInWindow();
                                   } 
                                   else {
                                          JOptionPane.showMessageDialog(null, "We don't have sufficient copies to issue the book.");
                                          BookNameBox.requestFocusInWindow();
                                   }
                            }

                            if (c == 0) {
                                   JOptionPane.showMessageDialog(null, "This book is not available in the library.");
                                   BookNameBox.requestFocusInWindow();
                            }

                            rs.close();
                            stmt.close();
                            con.close();
                     } 
                     catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "Some invalid value has been entered in the book name field.");
                     }
              } 
              else {
                     JOptionPane.showMessageDialog(null, "Please enter a value in the book name field.");
              }
       }

       public void SearchMember() {
              int c = 0;
              if (!MemberNameBox.getText().equals("")) {
                     try {

                            Class.forName("com.mysql.jdbc.Driver");

                            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");

                            Statement stmt = con.createStatement();

                            String query = "select memberNo, phone from members where memberName = '" + MemberNameBox.getText() + "';";

                            ResultSet rs = stmt.executeQuery(query);

                            while (rs.next()) {
                                   c = 1;
                                   MemberNumberLabel.setEnabled(true);
                                   MemberPhoneLabel.setEnabled(true);
                                   MemberNumberBox.setEnabled(true);
                                   PhoneBox.setEnabled(true);

                                   MemberNumberBox.setText(Integer.toString(rs.getInt(1)));
                                   PhoneBox.setText(rs.getString(2));
                                   NoOfCopiesBox.requestFocusInWindow();
                                   
                            }

                            if (c == 0) {
                                   JOptionPane.showMessageDialog(null, "This  name is not registered in the library.");
                                   MemberNameBox.requestFocusInWindow();
                            }

                            rs.close();
                            stmt.close();
                            con.close();

                     } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "Some invalid value has been entered in the member name field.");
                     }
              }
       }
       
