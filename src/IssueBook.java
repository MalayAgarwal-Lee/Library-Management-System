       private void IssueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IssueButtonActionPerformed
              if (!BookNameBox.getText().equals("") && !MemberNameBox.getText().equals("") && !NoOfCopiesBox.getText().equals("") && !DateOfIssueBox.getText().equals("")){
              
                     try {

                            Class.forName("com.mysql.jdbc.Driver");

                            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");

                            if (JOptionPane.showConfirmDialog(null, "Are you sure?") == JOptionPane.YES_OPTION) {

                                   String query1 = "insert into issues (bookNo, bookName, memberNo, memberName, phone, noOfCopies, dateOfIssue, leftCopies)" + "values (?, ?, ?, ?, ?, ?, ?, ?);";

                                   String doi = DateOfIssueBox.getText().substring(6, 10) + DateOfIssueBox.getText().substring(2, 6) + DateOfIssueBox.getText().substring(0, 2);

                                   PreparedStatement stmt = con.prepareStatement(query1);
                                   stmt.setString(1, BookNumberBox.getText());
                                   stmt.setString(2, BookNameBox.getText());
                                   stmt.setString(3, MemberNumberBox.getText());
                                   stmt.setString(4, MemberNameBox.getText());
                                   stmt.setString(5, PhoneBox.getText());
                                   stmt.setString(6, NoOfCopiesBox.getText());
                                   stmt.setString(7, doi);
                                   stmt.setString(8, NoOfCopiesBox.getText());

                                   stmt.execute();

                                   Statement stmt2 = con.createStatement();
                                   String query2 = "update books set balance = balance-" + NoOfCopiesBox.getText() + " where bookNo = " + BookNumberBox.getText() + ";";
                                   stmt2.executeUpdate(query2);

                                   JOptionPane.showMessageDialog(null, "Book issued successfully!");
                            
                                   stmt.close();
                                   stmt2.close();
                            } 
                     
                     Clear();
                     con.close();
                     } 
                     catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "A database connection error occured.");
                     }
              }
              else{
                     
                     JOptionPane.showMessageDialog(null, "One or more than one fields are empty.");
              }
       }//GEN-LAST:event_IssueButtonActionPerformed

