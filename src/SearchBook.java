       private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
            DefaultTableModel model = (DefaultTableModel)ResultTable.getModel();
            
            model.setRowCount(0);
            
            int c = 0;
            
            int index = OrderByCombo.getSelectedIndex();
              
            try{
                   
                   Class.forName("com.mysql.jdbc.Driver");
                   Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");
                   
                   Statement stmt = con.createStatement();
                   
                   String query = "";
                   
                   if (BookNumberRadioButton.isSelected()){
                          
                          if (!BookNumberBox.getText().equals("")){
                                 
                                 query = "select * from books where bookNo = " + BookNumberBox.getText() + ";";
                                 ResultSet rs = stmt.executeQuery(query);
                   
                                 while (rs.next()){
                          
                                          c = 1;
                                          model.addRow(new Object[] {rs.getString("bookNo"), rs.getString("bookName"), rs.getString("author"), rs.getString("publisher"), rs.getString("pages"), rs.getString("stock"), rs.getString("balance")});
                                 }
                                 
                                 if (c != 1){
                                        
                                          JOptionPane.showMessageDialog(null, "No books matched the search criteria.");
                                 }
                                 
                                 rs.close();
                          }
                          else{
                                 JOptionPane.showMessageDialog(null, "Please enter a value in the book number field.");
                          }
                          
                          
                   }
                   else if (SearchTermBox.isVisible() && !SearchTermBox.getText().equals("")){
                          
                          if (BookNameRadioButton.isSelected()){
                                 
                                 if (index == 0){
                                        query = "select * from books where bookName like '%%" + SearchTermBox.getText() + "%%' order by bookName;";
                                 }
                                 else{
                                        query = "select * from books where bookName like '%%" + SearchTermBox.getText() + "%%' order by bookName desc;";
                                 }
                          }
                          else if (AuthorRadioButton.isSelected()){
                                 
                                 if (index == 0){
                                        query = "select * from books where author like '%%" + SearchTermBox.getText() + "%%' order by author";
                                 }
                                 else{
                                        query = "select * from books where author like '%%" + SearchTermBox.getText() + "%%' order by author desc;"; 
                                 }
                          }
                          else if (PublisherRadioButton.isSelected()){
                                 if (index == 0){
                                        query = "select * from books where publisher like '%%" + SearchTermBox.getText() + "%%' order by publisher";
                                 }
                                 else{
                                        query = "select * from books where publisher like '%%" + SearchTermBox.getText() + "%%' order by publisher desc;";
                                 }
                          }
                          
                          ResultSet rs = stmt.executeQuery(query);
                   
                          while (rs.next()){
                          
                                 c = 1;
                                 model.addRow(new Object[] {rs.getString("bookNo"), rs.getString("bookName"), rs.getString("author"), rs.getString("publisher"), rs.getString("pages"), rs.getString("stock"), rs.getString("balance")});
                          }
                         
                          if (c != 1){
                                 JOptionPane.showMessageDialog(null, "No books matched the search criteria.");
                          }
                         rs.close();
                   }
                   else{
                          
                          if (!BookNumberRadioButton.isSelected() && !BookNameRadioButton.isSelected() && !AuthorRadioButton.isSelected() && !PublisherRadioButton.isSelected()){
                                 JOptionPane.showMessageDialog(null, "Please select a radio button.");
                          }
                          else{
                                 JOptionPane.showMessageDialog(null, "Please enter a value in the box.");
                          }
                   }
                   
                   stmt.close();
                   con.close();
            }
            catch(Exception e){
                   
                   JOptionPane.showMessageDialog(null, "A database connection error occured.");
            }
       }//GEN-LAST:event_SearchButtonActionPerformed

