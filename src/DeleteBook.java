       private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
              if (ResultTable.getSelectedRow() != -1){
                     
                     int row = ResultTable.getSelectedRow();
                     
                     try{
                            
                            Class.forName("com.mysql.jdbc.Driver");
                            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");
                            
                            if (JOptionPane.showConfirmDialog(null, "Are you sure?") == JOptionPane.YES_OPTION){
                                   
                                   Statement stmt = con.createStatement();
                                   
                                   String query = "delete from books where bookNo = " + model.getValueAt(row,  0) + ";";
                                   String query1 = "update books set bookNo = bookNo - 1 where bookNo > " + model.getValueAt(row, 0) + ";";
                                   String query2 = "delete from issues where bookNo = " + model.getValueAt(row,  0) + ";";
                                   String query3 = "update issues set bookNo = bookNo - 1 where bookNo > " + model.getValueAt(row, 0) + ";";
                                   String query4 = "delete from returns where bookNo = " + model.getValueAt(row,  0) + ";";
                                   String query5 = "update returns set bookNo = bookNo - 1 where bookNo > " + model.getValueAt(row, 0) + ";";
                                   String query6 = "delete from purchases where bookNo = " + model.getValueAt(row,  0) + ";";
                                   String query7 = "update purchases set bookNo = bookNo - 1 where bookNo > " + model.getValueAt(row, 0) + ";";
                                   
                                   stmt.executeUpdate(query);
                                   stmt.executeUpdate(query1);
                                   stmt.executeUpdate(query2);
                                   stmt.executeUpdate(query3);
                                   stmt.executeUpdate(query4);
                                   stmt.executeUpdate(query5);
                                   stmt.executeUpdate(query6);
                                   stmt.executeUpdate(query7);
                                   
                                   JOptionPane.showMessageDialog(null, "Book deleted successfully!");
                                   
                                   stmt.close();
                            }
                            
                            con.close();
                     }
                     catch(Exception e){
                            
                            JOptionPane.showMessageDialog(null, "A database connection error occured.");
                     }
              }
              else{
                     if (model.getRowCount() == 0){
                            
                            JOptionPane.showMessageDialog(null, "Please carry out a search before trying to delete a book.");
                     }
                     else{
                            
                            JOptionPane.showMessageDialog(null, "Please select a book from the table.");
                     }
              }
       }//GEN-LAST:event_DeleteButtonActionPerformed

