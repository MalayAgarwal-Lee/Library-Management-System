       private void PopulateResultTable() {
              try{
                     
                     Class.forName("com.mysql.jdbc.Driver");
                     Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");
                     
                     Statement stmt = con.createStatement();
                     
                     String query = "select memberNo, memberName, address, phone, DATE_FORMAT(dob, '%d-%m-%Y') from members;";
                     
                     ResultSet rs = stmt.executeQuery(query);
                     
                     while (rs.next()){
                            
                           model.addRow(new Object[] {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5)});
                     }
                     
                     rs.close();
                     stmt.close();
                     con.close();
              }
              catch(Exception e){
                     
                     JOptionPane.showMessageDialog(null, "A database connection error occured.");
              }
       }

