       private void SortByComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SortByComboBoxActionPerformed
              JComboBox cb = (JComboBox)evt.getSource();

              if (cb.getSelectedIndex() != -1){

                     int index = cb.getSelectedIndex();
                     EditButton.setEnabled(false);
                     DeleteButton.setEnabled(false);

                     try{

                            model.setRowCount(0);

                            Class.forName("com.mysql.jdbc.Driver");
                            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");

                            Statement stmt = con.createStatement();

                            String query = "";

                            switch (index) {

                                   case 1:
                                   query = "select memberNo, memberName, address, phone, DATE_FORMAT(dob, '%d-%m-%Y') from members order by memberName;";
                                   break;

                                   case 2:
                                   query = "select memberNo, memberName, address, phone, DATE_FORMAT(dob, '%d-%m-%Y') from members order by address;";
                                   break;

                                   case 3:
                                   query = "select memberNo, memberName, address, phone, DATE_FORMAT(dob, '%d-%m-%Y') from members order by phone;";
                                   break;

                                   case 4:
                                   query = "select memberNo, memberName, address, phone, DATE_FORMAT(dob, '%d-%m-%Y') from members order by dob;";
                                   break;
                                   
                                   default:
                                   query = "select memberNo, memberName, address, phone, DATE_FORMAT(dob, '%d-%m-%Y') from members order by memberNo;";
                                   break;
                            }

                            ResultSet rs = stmt.executeQuery(query);

                            while (rs.next()){

                                   model.addRow(new Object[] {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5)});
                            }

                            rs.close();
                            stmt.close();
                            con.close();
                     }
                     catch(Exception e){

                            JOptionPane.showMessageDialog(null, "A database connection error occured.");
                     }
              }
       }//GEN-LAST:event_SortByComboBoxActionPerformed

