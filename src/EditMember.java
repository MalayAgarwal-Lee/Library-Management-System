       private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
              EditClearButton.doClick();
              MembersComboBox.removeAllItems();
              try{
                     
                     Class.forName("com.mysql.jdbc.Driver");
                     Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");
                     
                     Statement stmt =  con.createStatement();
                     Statement stmt1 = con.createStatement();
                     
                     String query = "";
                     String count = "";
                     
                     if (SearchTermBox.isEnabled() && !SearchTermBox.getText().equals("")){
                            
                            if (MemberNumberRadioButton.isSelected()){
                                   
                                   query = "select memberName from members where memberNo = " + SearchTermBox.getText() + ";";
                                   count = "select count(*) from members where memberNo = " + SearchTermBox.getText() + ";";
                            }
                            else if (MemberNameRadioButton.isSelected()){
                                   
                                   query = "select memberName from members where memberName like '%%" + SearchTermBox.getText() + "%%';";
                                   count = "select count(*) from members where memberName like '%%" + SearchTermBox.getText() + "%%';";
                            }
                            else if (PhoneRadioButton.isSelected()){
                                   
                                   query = "select memberName from members where phone like '%%" + SearchTermBox.getText() + "%%';";
                                   count = "select count(*) from members where phone like '%%" + SearchTermBox.getText() + "%%';";
                            }
                            else{
                                   
                                   String dob = SearchTermBox.getText().substring(6, 10)+SearchTermBox.getText().substring(2, 6)+SearchTermBox.getText(0, 2);
                                   
                                   query = "select memberName from members where dob = '" + dob + "';";
                                   count = "select count(*) from members where dob = '" + dob + "';";
                            }
                            
                            ResultSet rs = stmt.executeQuery(query);
                            ResultSet rsCount = stmt1.executeQuery(count);
                            
                            int result = 0;
                            
                            while (rsCount.next()){
                                   
                                   result = rsCount.getInt(1);
                            }
                            
                            if (result != 0){
                                   
                                   MembersLabel.setEnabled(true);
                                   MembersComboBox.setEnabled(true);
                                   
                                   String[] model = new String[result+1];
                                   model[0] = "<Select>";
                                   int i = 1;
                                   
                                   while (rs.next()){
                                          
                                          model[i] = rs.getString(1);
                                          i++;
                                   }
                                   
                                   MembersComboBox.setModel(new DefaultComboBoxModel(model));
                            }
                            else{
                                   
                                   JOptionPane.showMessageDialog(null, "No members matched the search criteria.");
                            }
                            
                            rs.close();
                            rsCount.close();
                     }
                     else{
                            
                            if (!MemberNumberRadioButton.isSelected() && !MemberNameRadioButton.isSelected() && !PhoneRadioButton.isSelected() && !BirthRadioButton.isSelected()){
                                   
                                   JOptionPane.showMessageDialog(null, "Please select a radio button.");
                            }
                            else{
                                   
                                   JOptionPane.showMessageDialog(null, "Please enter a value in the box.");
                            }
                     }
                     
                     stmt.close();
                     stmt1.close();
                     con.close();
                     
              }
              catch (Exception e){
                     
                     JOptionPane.showMessageDialog(null, "A database connection error occured.");
              }
       }//GEN-LAST:event_SearchButtonActionPerformed

