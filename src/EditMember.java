       private void MembersComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MembersComboBoxActionPerformed
              JComboBox cb = (JComboBox)evt.getSource();
              
              if (cb.getSelectedIndex() != 0 && cb.getSelectedIndex() != -1){
                     
                     MemberNumberLabel.setEnabled(true);
                     MemberNumberBox.setEnabled(true);
                     MemberNameLabel.setEnabled(true);
                     MemberNameBox.setEnabled(true);
                     PhoneLabel.setEnabled(true);
                     PhoneBox.setEnabled(true);
                     BirthLabel.setEnabled(true);
                     BirthBox.setEnabled(true);
                     AddressLabel.setEnabled(true);
                     AddressBox.setEnabled(true);
                     
                     EditButton.setEnabled(true);
                     EditClearButton.setEnabled(true);
                     
                     MemberNameBox.requestFocusInWindow();
                     
                     try{
                            
                            Class.forName("com.mysql.jdbc.Driver");
                            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");
                            
                            Statement stmt = con.createStatement();
                            
                            String query = "select memberNo, memberName, phone, DATE_FORMAT(dob, '%d/%m/%Y'), address from members where memberName = '" + MembersComboBox.getSelectedItem().toString() + "';";
                            
                            ResultSet rs = stmt.executeQuery(query);
                            
                            while (rs.next()){
                                   
                                   MemberNumberBox.setText(rs.getString(1));
                                   MemberNameBox.setText(rs.getString(2));
                                   PhoneBox.setText(rs.getString(3));
                                   BirthBox.setText(rs.getString(4));
                                   AddressBox.setText(rs.getString(5));
                            }
                            
                            rs.close();
                            stmt.close();
                            con.close();     
                     }
                     catch (Exception e){
                            
                            JOptionPane.showMessageDialog(null, "A database connection error occured.");
                     }  
              }
              else{
                     
                     EditClearButton.doClick();
              }
       }//GEN-LAST:event_MembersComboBoxActionPerformed

