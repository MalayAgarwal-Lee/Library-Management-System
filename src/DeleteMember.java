       private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
              model.setRowCount(0);
              
              int c = 0;
              
              try{
                     
                     Class.forName("com.mysql.jdbc.Driver");
                     Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");
                     
                     Statement stmt = con.createStatement();
                     
                     String query = "";
                     
                     if (SearchTermBox.isEnabled() && !SearchTermBox.getText().equals("")){
                            
                            if (MemberNumberRadioButton.isSelected()){
                                   
                                   query = "select memberNo, memberName, address, DATE_FORMAT(dob, '%d-%m-%Y'), phone from members where memberNo = " + SearchTermBox.getText() + ";";
                            }
                            else if (MemberNameRadioButton.isSelected()){
                                   
                                   query = "select memberNo, memberName, address, DATE_FORMAT(dob, '%d-%m-%Y'), phone from members where memberName like '%%" + SearchTermBox.getText() + "%%';";
                            }
                            else if (BirthRadioButton.isSelected()){
                                   
                                   String dob = SearchTermBox.getText().substring(6, 10) + "-" + SearchTermBox.getText().substring(3, 5) + "-" + SearchTermBox.getText().substring(0, 2);
                                   query = "select memberNo, memberName, address, DATE_FORMAT(dob, '%d-%m-%Y'), phone from members where dob = '" + dob + "';";
                            }
                            else{
                                   
                                   query = "select memberNo, memberName, address, DATE_FORMAT(dob, '%d-%m-%Y'), phone from members where phone like '%%" + SearchTermBox.getText() + "%%';";
                            }
                            
                            ResultSet rs = stmt.executeQuery(query);
                            
                            while (rs.next()){
                                   
                                   c = 1;
                                   model.addRow(new Object[] {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5)});
                            }
                            
                            if (c != 1){
                                   
                                   JOptionPane.showMessageDialog(null, "No member matched the search criteria.");
                            }
                     }
                     else{
                            
                            if (!MemberNumberRadioButton.isSelected() && !MemberNameRadioButton.isSelected() && !BirthRadioButton.isSelected() && !PhoneRadioButton.isSelected()){
                                   
                                   JOptionPane.showMessageDialog(null, "Please select a radio button.");
                            }
                            else{
                                   
                                   JOptionPane.showMessageDialog(null, "Please enter a value in the box.");
                            }
                     }
              }
              catch(Exception e){
                     
                     JOptionPane.showMessageDialog(null, "A database connection error occured.");
              }
       }//GEN-LAST:event_SearchButtonActionPerformed

