       private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
              if (ResultTable.getSelectedRow() != -1){
                     
                     int row = ResultTable.getSelectedRow();
                     
                     try{
                            
                            Class.forName("com.mysql.jdbc.Driver");
                            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "password");
                            
                            if (JOptionPane.showConfirmDialog(null, "Doing this will remove the member from the entire database. Are you sure?") == JOptionPane.YES_OPTION){
                                   
                                   Statement stmt = con.createStatement();
                                   
                                   String query = "delete from members where memberNo = " + model.getValueAt(row,  0) + ";";
                                   String query1 = "update members set memberNo = memberNo - 1 where memberNo > " + model.getValueAt(row, 0) + ";";
                                   String query2 = "update books, issues set balance = stock where books.bookNo = issues.bookNo and memberNo = " + model.getValueAt(row, 0) + ";";
                                   String query3 = "delete from issues where memberNo = " + model.getValueAt(row,  0) + ";";
                                   String query4 = "update issues set memberNo = memberNo - 1 where memberNo > " + model.getValueAt(row, 0) + ";";
                                   String query5 = "delete from returns where memberNo = " + model.getValueAt(row,  0) + ";";
                                   String query6 = "update returns set memberNo = memberNo - 1 where memberNo > " + model.getValueAt(row, 0) + ";";
                                   
                                   
                                   stmt.executeUpdate(query);
                                   stmt.executeUpdate(query1);
                                   stmt.executeUpdate(query2);
                                   stmt.executeUpdate(query3);
                                   stmt.executeUpdate(query4);
                                   stmt.executeUpdate(query5);
                                   stmt.executeUpdate(query6);
                                   
                                   JOptionPane.showMessageDialog(null, "Member deleted successfully!");
                                   
                                   stmt.close();
                            }
                            
                            con.close();
                            
                     }
                     catch(Exception e){
                            
                            JOptionPane.showMessageDialog(null, "A database connection error occured.");
                     }
              }
              else{
                     
                     if (model.getRowCount() == 0){
                            
                            JOptionPane.showMessageDialog(null, "Please carry out a search before trying to delete a member.");
                     }
                     else{
                            
                            JOptionPane.showMessageDialog(null, "Please select a member to delete.");
                     }
              }
       }//GEN-LAST:event_DeleteButtonActionPerformed

